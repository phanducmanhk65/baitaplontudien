    public void inputFromFile() throws FileNotFoundException {
        File fil = new File("as.txt");
        Scanner sc = new Scanner(fil);
        while (sc.hasNext()) {
            //String word = sc.nextLine();
           // Scanner s = new Scanner(word);
            String st = sc.nextLine();
            model.addElement(st);
            String sn = sc.nextLine();

            hashMap.put(st, sn);
        }
    }
