        String Eng = ENGtext.getText();
         for (int i = 0; i < model.size(); i++) {
            // mode2.addElement(model.elementAt(i));
            int index = model.elementAt(i).indexOf(Eng);
            if (index == 0) {
                mode2.addElement(model.elementAt(i));
            }

        }
         jList1.setModel(mode2);
